<?php

$number = 11;

switch ($number) {
    case '12':
        echo 'Doze';
        break;
    case '13':
        echo 'Treze';
        break;
    case '14':
        echo 'Quatorze';
        break;
    case '15':
        echo 'Quinze';
        break;
    case '16':
        echo 'Dezeseis';
        break;
    case '11':
        echo 'Onze';
        break;

    default:
        echo 'Não identificado...';
        break;
}
